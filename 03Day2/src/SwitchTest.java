
public class SwitchTest {

	public static void main(String[] args) {
		String xxx2 = "A2";
		
		switch (xxx2) {
		case "A":
			System.out.println("A");
			break;
		case "A2":
			System.out.println("A2");
			break;
		case "A3":
			System.out.println("A3");
			break;	
		case "A4":
			System.out.println("A4");
			break;

		default:
			System.out.println("default");
			break;
		}
	}

}
